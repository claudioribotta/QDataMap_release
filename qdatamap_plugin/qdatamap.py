    def run_workflow(self):

        ################################################
        # Validate user input parameters
        msg = QMessageBox()
        msg.setWindowTitle("Warning")
        msg.setStandardButtons(QMessageBox.Ok)
        msg.setIcon(QMessageBox.NoIcon)

        # Validate output directory path
        if self.dlg.output_directory.text() == '':
            msg.setText("Please select the output directory to continue")
            msg.exec()
            return

        # Validate geospatial layer selection
        if self.dlg.select_folder_geospatial.text() == '':
            msg.setText("Please select a geospatial layer to continue")
            msg.exec()
            return

        # Validate tabular dataset selection
        if self.dlg.select_folder_csv.text() == '':
            msg.setText("Please select a tabular dataset to continue")
            msg.exec()
            return

        # Validate layout configuration
        page_size_setting = self.dlg.set_page_size.currentText()
        page_margin_setting = self.dlg.set_margins.currentText()
        layout_export_format = self.dlg.set_output_format.currentText()

        if page_size_setting == '' or page_margin_setting == '' or layout_export_format == '':
            proceed = self.ask_user_confirmation("Warning", "No page layout settings were found. Would you like to continue using the default layout?")
            if not proceed:
                return
            else:
                self.dlg.set_page_size.setCurrentIndex(0)
                self.dlg.set_margins.setCurrentIndex(0)
                self.dlg.set_output_format.setCurrentIndex(0)



        ################################################
        # Retrieve input parameters

        # Output directory path
        output_directory = self.dlg.output_directory.text()

        # Geospatial layer path and join field
        shape_folder_path = self.dlg.select_folder_geospatial.text()
        shape_name = self.dlg.select_file_geospatial.currentText()
        input_shape = os.path.join(shape_folder_path, shape_name)
        shape_join_field = self.dlg.join_field_geospatial.currentText()

        # Tabular dataset path and join field
        csv_folder_path = self.dlg.select_folder_csv.text()
        csv_join_field = self.dlg.join_field_csv.currentText()

        self.csv_count_success = 0
        self.csv_count_failed = 0


        ################################################
        # Execute workflow   
        
        if self.dlg.csv_iteration_checkbox.isChecked():
            # Count total CSV files for progress tracking
            csv_files = [f for f in os.listdir(csv_folder_path) if f.endswith(".csv")]
            total_csv = len(csv_files)

            # Ask user if they want preliminary join stats for batch processing
            reply = QMessageBox.question(
                None,
                "Batch Processing Options",
                "Do you want to run preliminary join statistics for each CSV file?\n\n"
                "This will check for unmatched records but will require user input for each CSV.",
                QMessageBox.Yes | QMessageBox.No,
                QMessageBox.No
            )
            run_prelim_stats = (reply == QMessageBox.Yes)

            self.init_progress_bar("Batch processing...", total_csv)

            import time
            batch_start_time = time.time()
            iteration_count = 0

            for file_name in csv_files:

                output_directory = self.dlg.output_directory.text()
                iteration_count += 1
                self.update_progress(iteration_count, f"Processing file {iteration_count}/{total_csv}: {file_name}")

                # Construct tabular dataset path
                input_csv = os.path.join(csv_folder_path, file_name)

                # Optionally run preliminary join stats
                if run_prelim_stats:
                    self.preliminary_join_stats(input_shape, shape_join_field, input_csv, csv_join_field)

                # Create output subdirectory
                subfolder_name = file_name.split('.')[0]
                output_directory = os.path.join(output_directory, subfolder_name)
                if not os.path.exists(output_directory):
                    os.makedirs(output_directory)

                # Execute workflow
                self.perform_the_work(input_csv, input_shape, output_directory)

            batch_elapsed = time.time() - batch_start_time
            print(f"[QDataMap] Batch completed ({total_csv} files): {batch_elapsed:.1f}s total ({batch_elapsed/total_csv:.1f}s avg per file)")

            self.close_progress_bar()
            self.dlg.accept()

            # Display processing summary
            self.iface.messageBar().pushMessage("Report", f"{self.csv_count_success} layouts exported successfully to {output_directory}. Failed to export {self.csv_count_failed} layouts", level=Qgis.Info, duration=10)

        else:
            csv_name = self.dlg.select_file_csv.currentText() + '.csv'
            input_csv = os.path.join(csv_folder_path, csv_name)

            # Validate spatial join compatibility
            proceed = self.preliminary_join_stats(input_shape, shape_join_field, input_csv, csv_join_field)
            if not proceed:
                return

            # Create output subdirectory
            if self.dlg.enable_subfolder_structure.isChecked():
                subfolder_name = csv_name.split('.')[0]
                output_directory = os.path.join(output_directory, subfolder_name)
                if not os.path.exists(output_directory):
                    os.makedirs(output_directory)

            # Initialize progress bar for single file processing
            self.init_progress_bar("Processing...", 11)

            # Execute workflow
            self.perform_the_work(input_csv, input_shape, output_directory, single_mode=True)

            self.close_progress_bar()
            self.dlg.accept()

            # Display processing summary
            self.iface.messageBar().pushMessage("Report", f"{self.csv_count_success} layouts exported successfully to {output_directory}. Failed to export {self.csv_count_failed} layouts", level=Qgis.Info, duration=10)




    def preliminary_join_stats(self, input_1_path, input_1_field, input_2_path, input_2_field):

        import chardet
        import csv

        # Load shapefile (input_1) with ogr provider
        input_1_layer = QgsVectorLayer(input_1_path, "input1", "ogr")

        # Load CSV (input_2) with proper encoding and delimiter detection
        csv_name = os.path.basename(input_2_path)

        # Detect file encoding
        with open(input_2_path, 'rb') as file:
            rawdata = file.read()
            encoding = chardet.detect(rawdata)['encoding']

        # Detect field delimiter
        with open(input_2_path, 'r', newline='', encoding=encoding) as f:
            sample = f.read(10000)
            dialect = csv.Sniffer().sniff(sample)
            delimiter = dialect.delimiter

        # Load CSV as delimited text layer
        uri = f"file:///{input_2_path}?delimiter={delimiter}"
        input_2_layer = QgsVectorLayer(uri, csv_name, 'delimitedtext')

        # Extract join field values from geospatial layer
        input1_keys = []
        for f in input_1_layer.getFeatures():
            input1_keys.append(f[input_1_field])
        input1_key_set = set(input1_keys)

        # Build frequency dictionary for tabular dataset join field
        input2_keys = []
        input2_freq = {}
        for f in input_2_layer.getFeatures():

            key = f[input_2_field]
            input2_keys.append(key)

            if key in input2_freq:
                input2_freq[key] += 1
            else:
                input2_freq[key] = 1

        # Compute intersection of join field values
        matched_keys = input1_key_set & set(input2_freq.keys())

        # Count unique matched features in geospatial layer
        matched_unique_input1 = sum(1 for k in input1_keys if k in matched_keys)

        # Calculate total output features after join operation
        total_matches = sum(input2_freq[k] for k in matched_keys)

        # Quantify geometry duplication
        duplicate_geometries = total_matches - matched_unique_input1

        # Calculate unmatched feature counts
        input1_count = len(input1_keys)
        input2_count = len(input2_keys)

        unmatched_input1 = input1_count - matched_unique_input1
        matched_input2 = sum(input2_freq[k] for k in matched_keys)
        unmatched_input2 = input2_count - matched_input2

        report = f"""
        Join by Field Value Diagnostics

        Geospatial dataset
            Total features: {input1_count}
                of which:
                    matched: {matched_unique_input1}
                    unmatched: {unmatched_input1}

        Tabular dataset
            Total features: {input2_count}
                of which:
                    matched: {matched_input2}
                    unmatched: {unmatched_input2}

        Join Results:
            Output features: {total_matches}
            Duplicate geometries: {duplicate_geometries}

        Click OK to run the join algorithm.
        """

        # Display join diagnostics report
        response = QMessageBox.information(self.dlg, "Join Report", report.strip(), QMessageBox.Ok | QMessageBox.Cancel)

        if response == QMessageBox.Cancel:
            return False
        elif response == QMessageBox.Ok:

            if duplicate_geometries != 0:
                proceed = self.ask_user_confirmation("Warning", f"{duplicate_geometries} geometries from the geospatial input will be duplicated due to multiple matches in the join field. Do you want to continue?")
                if not proceed:
                    return False
            
            if unmatched_input2 != 0:
                proceed = self.ask_user_confirmation("Warning", f"{unmatched_input2} features from the tabular dataset will be discarded because they do not match any value in the geospatial layer. Do you want to continue?")
                if not proceed:
                    return False
                                
            if unmatched_input1 != 0:
                proceed = self.ask_user_confirmation("Warning", f"{unmatched_input1} features from the geospatial input will be discarded if the 'Discard Nonmatching' option is selected, because they do not match any value in the tabular dataset. Do you want to continue?")
                if not proceed:
                    return False
                
            return True


    def perform_the_work(self, input_csv, input_shape, output_directory, single_mode=False):

        import time
        perform_start_time = time.time()

        # Import here to allow plugin initialization and dependency installation
        # Dependencies are verified by check_and_install_dependencies() before this function executes
        import chardet
        import matplotlib
        matplotlib.use('Agg')
        import matplotlib.pyplot as plt
        import pandas as pd
        import seaborn as sns

        # Retrieve QGIS project instance
        project = QgsProject.instance()

        # Step 1: Attribute table join
        self.update_progress(1 if single_mode else self.progress_bar.value(), "Joining datasets...")

        csv_name = os.path.basename(input_csv)

        # Detect file encoding
        with open(input_csv, 'rb') as file:
            rawdata = file.read()
            encoding = chardet.detect(rawdata)['encoding']

        # Detect field delimiter
        with open(input_csv, 'r', newline='', encoding=encoding) as f:
            sample = f.read(10000)
            dialect = csv.Sniffer().sniff(sample)
            delimiter = dialect.delimiter

        # Load CSV as delimited text layer
        uri = f"file:///{input_csv}?delimiter={delimiter}"
        input_csv_layer = QgsVectorLayer(uri, csv_name, 'delimitedtext')
        QgsProject.instance().addMapLayer(input_csv_layer, False)

        # Retrieve join field names
        shape_join_field = self.dlg.join_field_geospatial.currentText()
        csv_join_field = self.dlg.join_field_csv.currentText()

        # Define output path
        output_layer_name = csv_name.split('.')[0] + '.gpkg'
        output_layer_path = os.path.join(output_directory, output_layer_name)

        # Configure non-matching records option
        if self.dlg.discard_nonmatching_records.isChecked():
            discard_option = True
        else:
            discard_option = False

        # Configure spatial join parameters
        alg_params = {
        'INPUT':input_shape,
        'FIELD':shape_join_field,
        'INPUT_2':input_csv_layer,
        'FIELD_2':csv_join_field,
        'FIELDS_TO_COPY':[],
        'METHOD':1,
        'DISCARD_NONMATCHING':discard_option,
        'PREFIX':'',
        'OUTPUT': output_layer_path
        }

        join = processing.run("native:joinattributestable", alg_params)


        # Add joined layer to project

        layer = QgsVectorLayer(output_layer_path, csv_name.split('.')[0], 'ogr')
        
        QgsProject.instance().addMapLayer(layer, False)

        layer_tree_root = QgsProject.instance().layerTreeRoot()

        if self.dlg.set_dataviz_layer_rendering_position.text() == '':
            dataviz_layer_rendering_position = 0
        else:
            dataviz_layer_rendering_position = int(self.dlg.set_dataviz_layer_rendering_position.text())

        layer_tree_root.insertLayer(dataviz_layer_rendering_position, layer)
        layer.triggerRepaint()


        #############################################################
        # Step 2: Apply symbology
        self.update_progress(2 if single_mode else self.progress_bar.value(), "Applying symbology...")

        # Categorized symbology

        if self.dlg.select_render_methods.currentText() == 'Categorized':

            color_ramp = self.dlg.color_ramp_combobox.colorRamp()

            dataviz_field_name = self.dlg.select_dviz_field.currentField()
            dataviz_field = layer.fields().field(dataviz_field_name)


            if dataviz_field.type() == QVariant.String:
                pass
            else:
                proceed = self.ask_user_confirmation("Warning", "The selected field is not of type string. Do you want to continue?")
                if not proceed:
                    return

            unique_values = layer.uniqueValues(layer.fields().indexFromName(dataviz_field_name))
            unique_values = list(unique_values)

            num_values = len(unique_values)

            categories = []

            for i, value in enumerate(unique_values):

                symbol = QgsSymbol.defaultSymbol(layer.geometryType())

                if num_values == 0:
                    self.show_blocking_message_box("Warning", "No categoriess found in the selected field")
                elif num_values == 1:
                    color = color_ramp.color(0)
                else:
                    color = color_ramp.color(i / (num_values - 1))

                symbol.setColor(color)
                symbol.symbolLayer(0).setStrokeWidth(0.04)

                category = QgsRendererCategory(value, symbol, str(value))
                categories.append(category)

            renderer = QgsCategorizedSymbolRenderer(dataviz_field_name, categories)
            layer.setRenderer(renderer)

            layer.triggerRepaint()



        #############################################################
        # Graduated symbology

        if self.dlg.select_render_methods.currentText() == 'Graduated':

            color_ramp = self.dlg.color_ramp_combobox.colorRamp()
            dataviz_field_name = self.dlg.select_dviz_field.currentField()
            dataviz_field = layer.fields().field(dataviz_field_name)
            num_classes = self.dlg.select_num_classes.value()

            graduated_renderer = QgsGraduatedSymbolRenderer()
            graduated_renderer.setClassAttribute(dataviz_field_name)

            if self.dlg.select_render_modes.currentText() == 'Equal Count (Quantile)':
                classification_method = QgsClassificationQuantile()
                renderer_mode = QgsGraduatedSymbolRenderer.Quantile
                num_classes = int(num_classes)
                graduated_renderer.setClassificationMethod(classification_method)
                graduated_renderer.updateClasses(layer, renderer_mode, num_classes)


            elif self.dlg.select_render_modes.currentText() == 'Equal Interval':
                classification_method = QgsClassificationEqualInterval()
                renderer_mode = QgsGraduatedSymbolRenderer.EqualInterval
                num_classes = int(num_classes)
                graduated_renderer.setClassificationMethod(classification_method)
                graduated_renderer.updateClasses(layer, renderer_mode, num_classes)


            elif self.dlg.select_render_modes.currentText() == 'Fixed Interval':
                classification_method = QgsClassificationFixedInterval()
                renderer_mode = QgsGraduatedSymbolRenderer.Custom

                values = [f[dataviz_field_name] for f in layer.getFeatures() if f[dataviz_field_name] is not None]
                min_val, max_val = (min(values), max(values)) if values else (0, 0)
                interval_size = float(num_classes)
                num_classes = int((max_val - min_val) / interval_size) + 1 if interval_size > 0 else 1

                range_list = []
                for i in range(num_classes):
                    lower = min_val + i * interval_size
                    upper = lower + interval_size
                    label = f"{lower:.2f} – {upper:.2f}"
                    symbol = QgsSymbol.defaultSymbol(layer.geometryType())
                    range_list.append(QgsRendererRange(lower, upper, symbol, label))

                graduated_renderer = QgsGraduatedSymbolRenderer(dataviz_field_name, range_list)
                graduated_renderer.setSourceColorRamp(color_ramp)

            elif self.dlg.select_render_modes.currentText() == 'Logarithmic Scale':
                classification_method = QgsClassificationLogarithmic()
                num_classes = int(num_classes)

                ranges = classification_method.classes(layer, dataviz_field_name, num_classes)
                range_list = []
                for r in ranges:
                    symbol = QgsSymbol.defaultSymbol(layer.geometryType())
                    range_list.append(QgsRendererRange(r.lowerBound(), r.upperBound(), symbol, r.label()))

                graduated_renderer = QgsGraduatedSymbolRenderer(dataviz_field_name, range_list)
                graduated_renderer.setSourceColorRamp(color_ramp)


            elif self.dlg.select_render_modes.currentText() == 'Natural Breaks (Jenks)':
                classification_method = QgsClassificationJenks()
                renderer_mode = QgsGraduatedSymbolRenderer.Jenks
                num_classes = int(num_classes)
                graduated_renderer.setClassificationMethod(classification_method)
                graduated_renderer.updateClasses(layer, renderer_mode, num_classes)

            elif self.dlg.select_render_modes.currentText() == 'Pretty Breaks': 
                classification_method = QgsClassificationPrettyBreaks()
                renderer_mode = QgsGraduatedSymbolRenderer.Pretty
                num_classes = int(num_classes)
                graduated_renderer.setClassificationMethod(classification_method)
                graduated_renderer.updateClasses(layer, renderer_mode, num_classes)

            elif self.dlg.select_render_modes.currentText() == 'Standard Deviation':   
                classification_method = QgsClassificationStandardDeviation()
                renderer_mode = QgsGraduatedSymbolRenderer.StdDev
                num_classes = int(num_classes)
                graduated_renderer.setClassificationMethod(classification_method)
                graduated_renderer.updateClasses(layer, renderer_mode, num_classes)

            
            
            classification_method_name = graduated_renderer.classificationMethod().name()
            rules_based_renderer = QgsRuleBasedRenderer.convertFromRenderer(graduated_renderer)

            root_rule = rules_based_renderer.rootRule()

            # Fix field name quoting in filter expressions
            # convertFromRenderer() wraps numeric-looking field names in parentheses instead of quotes
            for rule in root_rule.children():
                filter_expr = rule.filterExpression()
                if filter_expr:
                    # Replace (field_name) with "field_name" for proper field reference
                    corrected_expr = filter_expr.replace(f'({dataviz_field_name})', f'"{dataviz_field_name}"')
                    rule.setFilterExpression(corrected_expr)

            # Separate empty range rules from valid rules
            zero_zero_rules = []
            non_zero_zero_rules = []

            for rule in root_rule.children():
                if rule.label() == '0 - 0':
                    zero_zero_rules.append(rule)
                else:
                    non_zero_zero_rules.append(rule)

            # Remove redundant empty range rules
            if len(non_zero_zero_rules) == 0:
                for rule in zero_zero_rules[1:]:
                    rules_based_renderer.rootRule().removeChild(rule)
            else:
                for rule in zero_zero_rules:
                    rules_based_renderer.rootRule().removeChild(rule)

            # Apply color ramp to classification rules
            if len(root_rule.children()) > 1:
                color_step = 0
                for s in range(0, len(root_rule.children())):
                    rule = root_rule.children()[s]
                    rule_symbol = rule.symbol()
                    rule_symbol.setColor(color_ramp.color(color_step))
                    color_step = color_step + (1/(len(root_rule.children())-1))
            else:
                rule = root_rule.children()[0]
                rule_symbol = rule.symbol()
                rule_symbol.setColor(color_ramp.color(0.5))

            # Get minimum value from data
            values = [f[dataviz_field_name] for f in layer.getFeatures() if f[dataviz_field_name] is not None]
            min_val = min(values) if values else 0

            # Fix first rule to use actual minimum value instead of rounded value
            if root_rule.children():
                first_rule = root_rule.children()[0]
                filter_expr = first_rule.filterExpression()
                if filter_expr and '>=' in filter_expr and ' AND ' in filter_expr:
                    # Split expression: "field" >= min AND "field" <= max
                    parts = filter_expr.split(' AND ')
                    upper_part = parts[1]  # Keep the upper bound part
                    # Reconstruct with actual minimum
                    corrected_expr = f'"{dataviz_field_name}" >= {min_val} AND {upper_part}'
                    first_rule.setFilterExpression(corrected_expr)

            # Create zero rule if minimum value is >= 0
            if min_val >= 0:
                # Modify first rule filter to exclude zero values
                root_rule = rules_based_renderer.rootRule()
                first_rule = root_rule.children()[0]
                filter_expression = first_rule.filterExpression()

                if '>=' in filter_expression:
                    new_filter_expression = filter_expression.replace('>=', '>')
                    first_rule.setFilterExpression(new_filter_expression)

                # Create symbol for zero values (white fill)
                zero_symbol = QgsSymbol.defaultSymbol(layer.geometryType())
                zero_symbol.setColor(QColor(255, 255, 255))

                # Create rule-based filter for zero values
                rule_zero = QgsRuleBasedRenderer.Rule(zero_symbol)
                rule_zero.setFilterExpression(f'"{dataviz_field_name}" = 0')
                rule_zero.setLabel("0")

                # Insert zero rule at first position
                rules_based_renderer.rootRule().insertChild(0, rule_zero)

            # Create hatched symbol for NULL values (white fill with diagonal lines)
            null_symbol = QgsSymbol.defaultSymbol(layer.geometryType())
            null_symbol.setColor(QColor(255, 255, 255))

            # Configure line pattern fill for NULL symbol
            line_pattern_fill = QgsLinePatternFillSymbolLayer()
            line_pattern_fill.setLineWidth(0.1)
            line_pattern_fill.setLineAngle(45.0)
            line_pattern_fill.setDistance(2.0)
            line_pattern_fill.setColor(QColor(130, 130, 130))
            null_symbol.appendSymbolLayer(line_pattern_fill)

            # Create rule-based filter for NULL values
            rule_null = QgsRuleBasedRenderer.Rule(null_symbol)
            rule_null.setFilterExpression(f'"{dataviz_field_name}" is NULL')
            rule_null.setLabel("Data not available")

            # Append NULL rule at last position
            rules_based_renderer.rootRule().appendChild(rule_null)

            # Update class labels to indicate exclusive lower bounds
            for rule in root_rule.children():
                class_label = rule.label()
                if '-' in class_label:
                    rule.setLabel(f'>{class_label}')

            # Remove invalid empty range rules
            for rule in root_rule.children():
                if rule.label() == '>0 - 0':
                    rules_based_renderer.rootRule().removeChild(rule)

            # Set stroke width for all symbols
            for rule in rules_based_renderer.rootRule().children():
                symbol = rule.symbol()
                symbol.symbolLayer(0).setStrokeWidth(0.04)

            # Apply renderer to layer
            layer.setRenderer(rules_based_renderer)
            layer.triggerRepaint()


        #############################################################
        # Step 3: Configure CRS transformation
        self.update_progress(3 if single_mode else self.progress_bar.value(), "Configuring CRS...")

        # Configure and apply project CRS transformation

        # Get user-selected and current CRS
        user_selected_crs = self.dlg.set_map_item_projection.crs()
        old_crs = QgsProject.instance().crs()

        # Configure transformation context with available coordinate operations
        transforms = QgsDatumTransform.operations(old_crs, user_selected_crs)
        if transforms:
            context = QgsProject.instance().transformContext()
            context.addCoordinateOperation(old_crs, user_selected_crs, transforms[0].proj)
            QgsProject.instance().setTransformContext(context)

        # Apply new CRS to project and reload layers
        QgsProject.instance().setCrs(user_selected_crs)
        QgsProject.instance().reloadAllLayers()

        # Adjust map canvas to display the reprojected layer extent
        layer_extent = layer.extent()
        transform = QgsCoordinateTransform(layer.crs(), user_selected_crs, QgsProject.instance())
        transformed_extent = transform.transformBoundingBox(layer_extent)

        self.iface.mapCanvas().setExtent(transformed_extent)
        self.iface.mapCanvas().refresh()


        #############################################################
        # Step 4: Load ancillary layers
        self.update_progress(4 if single_mode else self.progress_bar.value(), "Loading ancillary layers...")

        # Ancillary layers

        layer_tree_root = QgsProject.instance().layerTreeRoot()

        # Layer A: add to project, apply symbol, insert at specified position
        if self.dlg.set_layer_a.isChecked():
            layer_a_path = self.dlg.set_layer_a_path.text()
            layer_a_name = os.path.basename(layer_a_path).split('.')[0]
            layer_a = QgsVectorLayer(layer_a_path, layer_a_name, "ogr")

            layer_a_position = int(self.dlg.set_layer_a_rendering_position.text())
            QgsProject.instance().addMapLayer(layer_a, addToLegend=False)

            # Clone symbol to avoid memory issues during iteration
            layer_a_symbol = self.dlg.set_layer_a_style.symbol().clone()
            layer_a_renderer = layer_a.renderer()
            layer_a_renderer.setSymbol(layer_a_symbol)

            layer_tree_root.insertLayer(layer_a_position, layer_a)
            layer_a.triggerRepaint()
            self.iface.layerTreeView().refreshLayerSymbology(layer_a.id())
        else:
            layer_a = None

        # Layer B: add to project, apply symbol, insert at specified position
        if self.dlg.set_layer_b.isChecked():
            layer_b_path = self.dlg.set_layer_b_path.text()
            layer_b_name = os.path.basename(layer_b_path).split('.')[0]
            layer_b = QgsVectorLayer(layer_b_path, layer_b_name, "ogr")

            layer_b_position = int(self.dlg.set_layer_b_rendering_position.text())
            QgsProject.instance().addMapLayer(layer_b, addToLegend=False)

            # Clone symbol to avoid memory issues during iteration
            layer_b_symbol = self.dlg.set_layer_b_style.symbol().clone()
            layer_b_renderer = layer_b.renderer()
            layer_b_renderer.setSymbol(layer_b_symbol)

            layer_tree_root.insertLayer(layer_b_position, layer_b)
            layer_b.triggerRepaint()
            self.iface.layerTreeView().refreshLayerSymbology(layer_b.id())
        else:
            layer_b = None

        # Layer C: add to project, apply symbol, insert at specified position
        if self.dlg.set_layer_c.isChecked():
            layer_c_path = self.dlg.set_layer_c_path.text()
            layer_c_name = os.path.basename(layer_c_path).split('.')[0]
            layer_c = QgsVectorLayer(layer_c_path, layer_c_name, "ogr")

            layer_c_position = int(self.dlg.set_layer_c_rendering_position.text())
            QgsProject.instance().addMapLayer(layer_c, addToLegend=False)

            # Clone symbol to avoid memory issues during iteration
            layer_c_symbol = self.dlg.set_layer_c_style.symbol().clone()
            layer_c_renderer = layer_c.renderer()
            layer_c_renderer.setSymbol(layer_c_symbol)

            layer_tree_root.insertLayer(layer_c_position, layer_c)
            layer_c.triggerRepaint()
            self.iface.layerTreeView().refreshLayerSymbology(layer_c.id())
        else:
            layer_c = None

        # OpenStreetMap background layer
        if self.dlg.use_osm_background.isChecked():
            osm_url = "type=xyz&url=https://tile.openstreetmap.org/{z}/{x}/{y}.png"
            osm_layer = QgsRasterLayer(osm_url, "OpenStreetMap", "wms")
            QgsProject.instance().addMapLayer(osm_layer, False)
            layer_tree_root.insertLayer(len(layer_tree_root.children()), osm_layer)
            osm_layer.triggerRepaint()


        #############################################################
        # Map canvas extent

        self.iface.mapCanvas().setExtent(layer.extent())
        self.iface.mapCanvas().refresh()


        #############################################################
        # Step 5: Configure labels
        self.update_progress(5 if single_mode else self.progress_bar.value(), "Configuring labels...")

        # Label rendering

        # Initialize label settings
        label_settings = QgsPalLayerSettings()
        label_settings.isExpression = True
        label_settings.fieldName = self.dlg.labels_expression.expression()

        # Configure label placement
        placement_text = self.dlg.label_placement.currentText()

        if placement_text == 'Around Point':
            label_settings.placement = Qgis.LabelPlacement.AroundPoint
        elif placement_text == 'Over Point':
            label_settings.placement = Qgis.LabelPlacement.OverPoint
        elif placement_text == 'Horizontal':
            label_settings.placement = Qgis.LabelPlacement.Horizontal
        elif placement_text == 'Free':
            label_settings.placement = Qgis.LabelPlacement.Free
        elif placement_text == 'Perimeter Curved':
            label_settings.placement = Qgis.LabelPlacement.PerimeterCurved
        elif placement_text == 'Outside Polygons':
            label_settings.placement = Qgis.LabelPlacement.OutsidePolygons
        elif placement_text == 'Pole of Inaccessibility':
            label_settings.geometryGeneratorEnabled = True
            label_settings.geometryGeneratorType = QgsWkbTypes.PolygonGeometry
            label_settings.geometryGenerator = 'pole_of_inaccessibility($geometry, 0.1)'
        else:
            label_settings.placement = Qgis.LabelPlacement.Free


        # Apply text format from user settings
        text_format = self.dlg.labels_font.textFormat()
        label_settings.setFormat(text_format)

        # Enable labeling and apply to layer
        label_settings.enabled = True
        labeling = QgsVectorLayerSimpleLabeling(label_settings)
        layer.setLabeling(labeling)
        layer.setLabelsEnabled(True)
        layer.triggerRepaint()



        #############################################################
        # Step 6: Create print layout
        self.update_progress(6 if single_mode else self.progress_bar.value(), "Creating print layout...")

        # Print layout creation

        # Initialize print layout
        project = QgsProject.instance()
        layout = QgsPrintLayout(project)
        layout.initializeDefaults()

        layout_name = output_layer_name.split('.')[0] + '_' + dataviz_field_name
        layout.setName(layout_name)

        # Configure page dimensions
        if self.dlg.set_page_size.currentText() == 'custom (mm)':
            h = int(self.dlg.custom_page_heigth.text())
            w = int(self.dlg.custom_page_width.text())
        elif self.dlg.set_page_size.currentText() == 'A4 vertical':
            h = 297
            w = 210
        elif self.dlg.set_page_size.currentText() == 'A4 horizontal':
            h = 210
            w = 297
        elif self.dlg.set_page_size.currentText() == 'A3 vertical':
            h = 420
            w = 297
        elif self.dlg.set_page_size.currentText() == 'A3 horizontal':
            h = 297
            w = 420

        page_size = QgsLayoutSize(w, h, QgsUnitTypes.LayoutMillimeters)
        layout.pageCollection().pages()[0].setPageSize(page_size)
        project.layoutManager().addLayout(layout)

        # Configure page margins
        if self.dlg.set_margins.currentText() == 'small':
            margins = 2
        elif self.dlg.set_margins.currentText() == 'medium':
            margins = 4
        elif self.dlg.set_margins.currentText() == 'large':
            margins = 8


        ##################################
        # Title item

        title_text = None
        
        # Get the expression string from the widget
        title_exp_str = self.dlg.set_map_title_expression.expression()
        
        if title_exp_str:
            try:
                if "@_tabular_dataset_name" in title_exp_str or "@_geospatial_layer_name" in title_exp_str:
                    csv_display_name =  csv_name.split('.')[0]
                    shape_name = os.path.basename(input_shape)
                    geo_display_name = shape_name.split('.')[0]

                    # Replace variables in the expression string
                    if "@_tabular_dataset_name" in title_exp_str:
                        
                        # Replace the ENTIRE placeholder including quotes with a proper string
                        title_exp_str = title_exp_str.replace('"@_tabular_dataset_name"', f"'{csv_display_name}'")
                        # Also handle case without quotes
                        title_exp_str = title_exp_str.replace('@_tabular_dataset_name', f"'{csv_display_name}'")

                    if "@_geospatial_layer_name" in title_exp_str:
                        # Replace the ENTIRE placeholder including quotes with a proper string
                        title_exp_str = title_exp_str.replace('"@_geospatial_layer_name"', f"'{geo_display_name}'")
                        # Also handle case without quotes
                        title_exp_str = title_exp_str.replace('@_geospatial_layer_name', f"'{geo_display_name}'")


            
                # Evaluate the expression - project variables are automatically available!
                title_exp = QgsExpression(title_exp_str)
                
                context = QgsExpressionContext()
                context.appendScope(QgsExpressionContextUtils.globalScope())
                context.appendScope(QgsExpressionContextUtils.projectScope(QgsProject.instance()))
                context.appendScope(QgsExpressionContextUtils.layerScope(layer))
                feature = next(layer.getFeatures())
                context.setFeature(feature)

                value = title_exp.evaluate(context)

                if title_exp.hasEvalError():
                    raise Exception(title_exp.evalErrorString())

                title_text = str(value) if value is not None else None
                
            except Exception as e:
                QgsMessageLog.logMessage(f"Title expression error: {e}", "QDataMap", Qgis.Warning)
        
        if not title_text:
            title_text = self.dlg.set_map_title_expression.currentText()


        if title_text:

            # Create title label item
            title = QgsLayoutItemLabel(layout)

            title.setText(title_text)

            # Apply text format
            title_text_format = self.dlg.set_title_font.textFormat()
            title.setTextFormat(title_text_format)

            # Set label position aligned with map margins
            title_x_position = margins
            title_y_position = margins

            title.attemptMove(QgsLayoutPoint(title_x_position, title_y_position, QgsUnitTypes.LayoutMillimeters))

            # Set label dimensions
            title.adjustSizeToText()
            title_x_size = w*(2/3)
            title_y_size = title.sizeForText().height()
            title.attemptResize(QgsLayoutSize(title_x_size, title_y_size, QgsUnitTypes.LayoutMillimeters))

            # subheading alignment
