    def suggest_class_number_or_size(self):

        #############################################################
        # Calculate optimal classification parameters based on data distribution

        import chardet
        import pandas as pd

        csv_folder_path = self.dlg.select_folder_csv.text()
        csv_name = self.dlg.select_file_csv.currentText() + '.csv'
        input_csv = os.path.join(csv_folder_path, csv_name)

        if self.dlg.csv_iteration_checkbox.isChecked():
            # Batch mode: use default values
            self.dlg.select_num_classes.setValue(1)
            self.dlg.select_num_classes_label.setText('Classes/Size')

        else:
            if self.dlg.select_render_methods.currentText() == 'Graduated':
                # Detect encoding and load CSV
                with open(input_csv, 'rb') as file:
                    rawdata = file.read()
                    encoding = chardet.detect(rawdata)['encoding']

                df = pd.read_csv(input_csv, sep=None, engine='python', encoding=encoding)

                # Extract classification field values
                field_name = self.dlg.select_dviz_field.currentText()
                values = df[field_name].dropna().tolist()

                # Compute descriptive statistics
                if values:
                    min_val = min(values)
                    max_val = max(values)
                    n = len(values)

                # Square root rule for class count estimation
                number_of_classes = int(n ** 0.5)
                if (n ** 0.5) > number_of_classes:
                    number_of_classes += 1

                # Calculate interval size
                interval_size = (max_val - min_val) / number_of_classes

                # Round to aesthetically pleasing breaks
                if interval_size < 5:
                    pretty_interval = int(interval_size)
                elif interval_size < 10:
                    pretty_interval = 5
                else:
                    magnitude = 10 ** (len(str(int(interval_size))) - 1)
                    pretty_interval = round(interval_size / magnitude) * magnitude

                # Round interval size for display
                if interval_size < 1:
                    rounded_interval_size = round(interval_size, 1)
                else:
                    rounded_interval_size = round(interval_size)

                # Ensure minimum pretty interval
                if pretty_interval < 1:
                    pretty_interval = 0.1

                # Limit maximum class count for readability
                if number_of_classes > 10:
                    number_of_classes = 10

                # Apply suggested values based on classification method
                if self.dlg.select_render_modes.currentText() == 'Equal Count (Quantile)':
                    self.dlg.select_num_classes_label.setText('Classes')
                    self.dlg.select_num_classes.setValue(number_of_classes)

                elif self.dlg.select_render_modes.currentText() == 'Equal Interval':
                    self.dlg.select_num_classes_label.setText('Classes')
                    self.dlg.select_num_classes.setValue(number_of_classes)

                elif self.dlg.select_render_modes.currentText() == 'Fixed Interval':
                    self.dlg.select_num_classes_label.setText('Interval size')
                    self.dlg.select_num_classes.setValue(rounded_interval_size)

                elif self.dlg.select_render_modes.currentText() == 'Logarithmic Scale':
                    self.dlg.select_num_classes_label.setText('Classes')
                    self.dlg.select_num_classes.setValue(number_of_classes)

                elif self.dlg.select_render_modes.currentText() == 'Natural Breaks (Jenks)':
                    self.dlg.select_num_classes_label.setText('Classes')
                    self.dlg.select_num_classes.setValue(number_of_classes)

                elif self.dlg.select_render_modes.currentText() == 'Pretty Breaks':
                    self.dlg.select_num_classes_label.setText('Classes')
                    self.dlg.select_num_classes.setValue(number_of_classes)

                elif self.dlg.select_render_modes.currentText() == 'Standard Deviation':
                    self.dlg.select_num_classes_label.setText('Classes')
                    self.dlg.select_num_classes.setValue(number_of_classes)



