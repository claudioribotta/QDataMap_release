    def csv_iteration_setting(self):

        #############################################################
        # Validate and configure batch processing mode for multiple CSV files

        import chardet
        import pandas as pd

        if self.dlg.csv_iteration_checkbox.isChecked():
            self.dlg.select_render_methods.setCurrentText("Categorized")

            # Disable single file selection in batch mode
            self.dlg.select_file_csv.clear()
