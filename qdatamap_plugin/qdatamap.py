            distance_area.setSourceCrs(project_crs, QgsProject.instance().transformContext())
            distance_area.setEllipsoid(QgsProject.instance().ellipsoid())

            # Calculate horizontal distance
            point_left = QgsPointXY(extent.xMinimum(), extent.yMinimum())
            point_right = QgsPointXY(extent.xMaximum(), extent.yMinimum())
            width_meters = distance_area.measureLine([point_left, point_right])

            # Calculate vertical distance
            point_bottom = QgsPointXY(extent.xMinimum(), extent.yMinimum())
            point_top = QgsPointXY(extent.xMinimum(), extent.yMaximum())
            height_meters = distance_area.measureLine([point_bottom, point_top])

            # Use maximum extent dimension
            max_extent = max(width_meters, height_meters)

            # Get CRS units from project CRS (not layer CRS)
            crs_units = project_crs.mapUnits()

            # Convert to meters based on CRS units
            if crs_units == QgsUnitTypes.DistanceDegrees:
                # Geographic CRS: approximate 1 degree ~ 111 km
                max_extent_meters = max_extent * 111 * 1000
            elif crs_units == QgsUnitTypes.DistanceMeters:
                max_extent_meters = max_extent
            elif crs_units == QgsUnitTypes.DistanceKilometers:
                max_extent_meters = max_extent * 1000
            elif crs_units == QgsUnitTypes.DistanceFeet:
                max_extent_meters = max_extent * 0.3048
            elif crs_units == QgsUnitTypes.DistanceMiles:
                max_extent_meters = max_extent * 1609.34
            elif crs_units == QgsUnitTypes.DistanceYards:
                max_extent_meters = max_extent * 0.9144
            else:
                max_extent_meters = max_extent

            # Convert to kilometers
            max_extent_km = max_extent_meters / 1000.0

