            scalebar_height = scale_bar.rectWithFrame().height()
            scale_bar.attemptResize(QgsLayoutSize(scalebar_fixed_width, scalebar_height, QgsUnitTypes.LayoutMillimeters))
            scale_bar.update()
            scale_bar.refresh()

