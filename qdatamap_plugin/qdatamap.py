    def setup_groupbox_accordion(self):

        #############################################################
        # Configure accordion behavior for collapsible groupboxes

        from qgis.PyQt.QtCore import QEvent

        # Collapsible groupbox references
        self.groupboxes = [
            self.dlg.groupbox_map_item_settings,
            self.dlg.groupbox_layout_headers,
            self.dlg.groupbox_ancillary_layers,
            self.dlg.groupbox_ancillary_items
        ]

        # Collapse all groupboxes except the active one
        def collapse_others(active_gb):
            if not active_gb.isCollapsed():
                for gb in self.groupboxes:
                    if gb != active_gb:
                        gb.setCollapsed(True)

        # Event filter to trigger accordion behavior
        class InlineEventFilter(QObject):
            def eventFilter(inner_self, obj, event):
                if event.type() in (QEvent.Show, QEvent.Resize, QEvent.LayoutRequest):
                    collapse_others(obj)
                return False

        # Install event filter on all groupboxes
        self._groupbox_filter = InlineEventFilter()
        for gb in self.groupboxes:
            gb.installEventFilter(self._groupbox_filter)


